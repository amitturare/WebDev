// Math
// Standard built-in objects - always available

console.log(Math.floor(4.122)); // Rounds off the number down
console.log(Math.ceil(4.4)); // Rounds off the number up
console.log(Math.sqrt(36)); // Finds square root of a number
console.log(Math.PI); // pi value = 22/7 == 3.14
console.log(Math.min(1, 2, 3, 4, 5)); // returns the minimum value
console.log(Math.max(1, 2, 3, 4, 5)); // returns the maximum value
console.log(Math.random()); // returns a random number between 0 and 1
console.log(Math.random() * 10); // returns a random number between 0 and 10
console.log(Math.random() * 100); // returns a random number between 0 and 100
console.log(Math.floor(Math.random() * 100)); // to returns a random whole number number between 0 and 100
