// Template Literals - ES6+
// Backtick characters `` 
// Interpolation ${} - insert expression(value)

// 
const firstName = 'Amit'
const age = 20
const sentence = "Hey it's " + firstName + ' and he is ' + age + ' years old'

const value = `Hey it's ${firstName} and he is ${age} years old and here is some simple math ${5 + 6}`; // this is an example of Template Literals

console.log(sentence);
console.log(value);